    private void tbldanhsachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldanhsachMouseClicked
        // TODO add your handling code here:
        try {
            int index = tbldanhsach.getSelectedRow();
            TableModel model = tbldanhsach.getModel();
            txtid.setText(model.getValueAt(index, 0).toString());
            txtname.setText(model.getValueAt(index, 1).toString());
            txtamount.setText(model.getValueAt(index, 2).toString());
            //type
            for(Map.Entry<Integer,String> entry : map.entrySet()){
                if(entry.getKey() == Integer.parseInt(model.getValueAt(index, 3).toString())){
                    cbbtype.setSelectedItem(entry.getValue());
                }
            }
            txtimprice.setText(model.getValueAt(index, 4).toString());
            txtexprice.setText(model.getValueAt(index,5).toString());
            //date
            Date date;
            date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(index, 6).toString());
            txtdate.setDate(date);
            //image
            BufferedImage image = null;
            ImageIcon icon = null;
            try {
                txtphotopath.setText(model.getValueAt(index, 7).toString());
                image = ImageIO.read(new File(txtphotopath.getText()));
                icon = new ImageIcon(image.getScaledInstance(177, 122, image.SCALE_SMOOTH));
                lblimage.setIcon(icon);
                
                
                
            } catch (IOException ex) {
                Logger.getLogger(EquipmentJpanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                txtBarcode.setText(model.getValueAt(index, 8).toString());
                image = ImageIO.read(new File(txtBarcode.getText()));
                icon = new ImageIcon(image.getScaledInstance(177, 122, image.SCALE_SMOOTH));
                jlbBarcode.setIcon(icon);
            } catch (IOException ex) {
                Logger.getLogger(EquipmentJpanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            //barcode
            
        } catch (ParseException ex) {
            Logger.getLogger(EquipmentJpanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbldanhsachMouseClicked

