    private void btfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btfindActionPerformed
        // TODO add your handling code here:
        EquipmentObject eo1 = new EquipmentObject();
        if(rdname.isSelected()){
            if(!txtname.getText().isEmpty()){
            eo1.setEquipment_name(txtname.getText());
            ResultSet rs = e.getEquipments(cond.getEquipmentByNameOrByEquipmentTypeId(eo1));
            tbldanhsach.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                JOptionPane.showMessageDialog(null,"Bạn phải nhập tên cần tìm");
            }
        }
        else if(rdloai.isSelected()){
            EquipmentObject eo2 = new EquipmentObject();
            for(Map.Entry<Integer,String> entry: map.entrySet()){
                if(entry.getValue() == cbbtype.getSelectedItem()){
                    eo2.setEquipment_type_id(entry.getKey());
                    ResultSet rs = e.getEquipments(cond.getEquipmentByNameOrByEquipmentTypeId(eo2));
                    tbldanhsach.setModel(DbUtils.resultSetToTableModel(rs));
                }
            }
        }
        else if(rdid.isSelected()){
            if(!txtid.getText().isEmpty()){
            ResultSet rs = e.getEquipment(Integer.parseInt(txtid.getText()));
            tbldanhsach.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                JOptionPane.showMessageDialog(null,"Bạn phải nhập tên cần tìm");
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Bạn phải chọn mục cần tìm");
        }
    }//GEN-LAST:event_btfindActionPerformed

