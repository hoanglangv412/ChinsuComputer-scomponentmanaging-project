    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
        // TODO add your handling code here:
        if(txtname.getText().trim().isEmpty() || txtaddress.getText().trim().isEmpty()||txtphone.getText().trim().isEmpty() || txtimamount.getText().trim().isEmpty()||txtid.getText().trim().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập đủ dữ liệu");
        } else {
            DistributorObject dis = new DistributorObject();
            dis.setDistributor_id(Integer.parseInt(txtid.getText()));
            dis.setDistributor_name(txtname.getText());
            dis.setDistributor_address(txtaddress.getText());
            dis.setDistributor_phone(txtphone.getText());
            dis.setDistributor_imamount(Integer.parseInt(txtimamount.getText()));
            if(d.editDistributor(dis)) {
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Nhà phân phối này không tồn tại");
            }
        }
        btreloadActionPerformed(evt);
    }//GEN-LAST:event_bteditActionPerformed

