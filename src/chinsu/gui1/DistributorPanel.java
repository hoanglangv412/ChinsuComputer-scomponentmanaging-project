    private void btfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btfindActionPerformed
        // TODO add your handling code here:
        DistributorObject obj = new DistributorObject();
        if(rdname.isSelected()){
            if(!txtname.getText().isEmpty()){
            obj.setDistributor_name(txtname.getText());
            ResultSet rs = d.getDistributors(cond.getDistributorByNameOrAddressOrPhone(obj));
            tbldanhsach.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên nhà phân phối để tìm");
            }
        }
        else if(rdaddress.isSelected()){
            if(!txtaddress.getText().isEmpty()){
                obj.setDistributor_address(txtaddress.getText());
                ResultSet rs = d.getDistributors(cond.getDistributorByNameOrAddressOrPhone(obj));
                tbldanhsach.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                JOptionPane.showMessageDialog(null, "Bạn chưa nhập địa chỉ nhà phân phối để tìm.");
            }
        }
        else if(rdphone.isSelected()){
            if(!txtphone.getText().isEmpty()){
                obj.setDistributor_phone(txtphone.getText());
                ResultSet rs = d.getDistributors(cond.getDistributorByNameOrAddressOrPhone(obj));
                tbldanhsach.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                JOptionPane.showMessageDialog(null, "Bạn chưa nhập số điện thoại nhà phân phối để tìm");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn thông tin cần tìm ?");
        }
    }//GEN-LAST:event_btfindActionPerformed


