    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtTenDangNhap.getText().trim().isEmpty() || txtMatKhau.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Không được để trống tài khoản và mật khẩu");
        } else {
            UserObject us = new UserObject();
            us.setUser_name(txtTenDangNhap.getText());
            us.setUser_pass(txtMatKhau.getText());
            if(rdAdmin.isSelected()) {
                us.setUser_permission(0);
            }
            if(rdNhanVienKho.isSelected()) {
                us.setUser_permission(1);
            }
            if(rdNhanVien.isSelected()) {
                us.setUser_permission(2);
            }
            if(u.addUser(us)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Tài khoản đã tồn tại");
            }
        }
        btnReloadActionPerformed(evt);
    }//GEN-LAST:event_jButton2ActionPerformed

