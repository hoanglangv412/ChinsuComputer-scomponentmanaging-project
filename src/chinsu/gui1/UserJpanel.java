    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(txtTenDangNhap.getText().trim().isEmpty() || txtMatKhau.getText().trim().isEmpty() || txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Không được để trống id, tài khoản và mật khẩu");
        } else {
            UserObject us = new UserObject();
            us.setUser_id(Integer.parseInt(txtId.getText()));
            us.setUser_name(txtTenDangNhap.getText());
            us.setUser_pass(txtMatKhau.getText());
            if(rdAdmin.isSelected()) {
                us.setUser_permission(0);
            }
            if(rdNhanVienKho.isSelected()) {
                us.setUser_permission(1);
            }
            if(rdNhanVien.isSelected()) {
                us.setUser_permission(2);
            }
            if(u.editUser(us)) {
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Tài khoản không tồn tại");
            }
        }
        btnReloadActionPerformed(evt);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nhập id cần xóa");
        } else {
            UserObject us = new UserObject();
            us.setUser_id(Integer.parseInt(txtId.getText()));
            if(u.delUser(us)) {
                JOptionPane.showMessageDialog(null, "Xóa thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi!!!");
            }
        }
        btnReloadActionPerformed(evt);
    }//GEN-LAST:event_jButton4ActionPerformed

