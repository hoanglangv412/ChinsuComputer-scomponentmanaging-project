    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (txtTenLoai.getText().trim().isEmpty() || txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Không được để trống id, tên loại");
        } else {
            TypeObject to = new TypeObject();
            to.setType_id(Integer.parseInt(txtId.getText()));
            to.setType_name(txtTenLoai.getText());
            String[] dis = cbDistributor.getSelectedItem().toString().split("-");
            to.setType_distributor_id(Integer.parseInt(dis[0]));
            if (t.editType(to)) {
                JOptionPane.showMessageDialog(null, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Loại hàng không tồn tại");
            }
        }
        btnReloadActionPerformed(evt);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nhập id cần xóa");
        } else {
            TypeObject to = new TypeObject();
            to.setType_id(Integer.parseInt(txtId.getText()));
            if (t.delType(to)) {
                JOptionPane.showMessageDialog(null, "Xóa thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi!!!");
            }
        }
        btnReloadActionPerformed(evt);
    }//GEN-LAST:event_jButton4ActionPerformed

