    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txtTenLoai.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Không được để trống tên loại");
        } else {
            TypeObject to = new TypeObject();
            to.setType_name(txtTenLoai.getText());
            String[] dis = cbDistributor.getSelectedItem().toString().split("-");
            to.setType_distributor_id(Integer.parseInt(dis[0]));
            if (t.addType(to)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Loại hàng đã tồn tại");
            }
        }
        btnReloadActionPerformed(evt);
    }//GEN-LAST:event_jButton2ActionPerformed

